<h1 class="page-header">Reclamações do Consumidor</h1>

<h2 class="sub-header">Dados</h2>

<p>
  Os dados utilizados para este trabalho foram obtidos em
  <a href="http://dados.gov.br/dataset/reclamacoes-do-consumidor-gov-br">
    http://dados.gov.br/dataset/reclamacoes-do-consumidor-gov-br</a> e contém
  registros nacionais de reclamações de usuários de Agosto de 2014 a Janeiro de
  2015 sobre os serviços prestados por diversas empresas. A base é mantida pela
  SENACON - Secretaria Nacional do Consumidor.
</p>

<p>
  Os dados foram corretamente normalizados e separados nas tabelas gerando mais
  de 94700 registros. Existem dados de mais de 200 empresas e mais de 45000
  reclamações de consumidores foram analizadas.
</p>

<h2 class="sub-header">Instruções</h2>
<p>
  No menu lateral encontram-se as queries que foram escritas para o trabalho.
  Cada link representa uma query. Ao clicar nelas, uma tela se abrirá contendo
  o SQL, os resultados e algum eventual conteúdo dinâmico de cada uma das
  consultas.
</p>

<p>
  No menu superior encontram-se informações adicionais sobre o trabalho bem como
  um ambiente parecido com o <em>Praticando SQL</em> que utilizamos durante o
  semestre, porém com a base de dados que escolhemos para o nosso trabalho.
</p>

